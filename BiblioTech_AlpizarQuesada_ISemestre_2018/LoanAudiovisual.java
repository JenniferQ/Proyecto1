    private void btn_registerLoanAudiovisualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registerLoanAudiovisualActionPerformed
        archivoLoan = new File("Loan.dat");
        try {
            String formato = fechaInicialAudiovisual.getDateFormatString();
            Date fechainicial = fechaInicialAudiovisual.getDate();
            SimpleDateFormat sdf = new SimpleDateFormat(formato);

            //            String format=fechaFinal.getDateFormatString();
            //            Date fechafinal= fechaFinal.getDate();
            //            SimpleDateFormat simDateF= new SimpleDateFormat(format);
            //
            loanFile = new LoanFile(archivoLoan);
            Loan loan = new Loan(tf_nameLoan.getText(), tf_idLoanAudiovisual.getText(), tf_codeloanAudiovisual.getText(),
                    "" + sdf.format(fechainicial));

            loanFile.addEndRecord(loan);
            lb_messageLoan.setVisible(true);
            lb_messageLoan.setText("The loan originated correctly");
            if (loanFile.quantityAvailable(tf_codeloanAudiovisual.getText())) {

            } else {
                lb_messageLoan.setText("The audiovisual is not available");
            }

            tf_codeloanAudiovisual.setText("");
            tf_idLoanAudiovisual.setText("");
            tf_nameLoan.setText("");
            tfx_searchAudiovisual.setText("");

        } catch (FileNotFoundException ex) {
            Logger.getLogger(LoanBook.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(LoanBook.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btn_registerLoanAudiovisualActionPerformed

