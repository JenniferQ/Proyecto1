    private void tfx_searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfx_searchKeyReleased
        txa_information.setText("");

        String cadenainformativa = "";
        for (int i = 0; i < book.size(); i++) {
            if (book.get(i).search(tfx_search.getText().toUpperCase())) {
                cadenainformativa += "\n" + book.get(i).toString() + "\n";
            }
        }
        txa_information.setText(cadenainformativa);


    }//GEN-LAST:event_tfx_searchKeyReleased

