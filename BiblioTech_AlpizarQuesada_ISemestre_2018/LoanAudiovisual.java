    private void tfx_searchAudiovisualKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfx_searchAudiovisualKeyReleased
        txa_information.setText("");

        String cadenainformativa = "";
        for (int i = 0; i < audiovisualArray.size(); i++) {
            if (audiovisualArray.get(i).search(tfx_searchAudiovisual.getText().toUpperCase())) {
                cadenainformativa += "\n" + audiovisualArray.get(i).toString() + "\n";
            }
        }
        txa_information.setText(cadenainformativa);

    }//GEN-LAST:event_tfx_searchAudiovisualKeyReleased

