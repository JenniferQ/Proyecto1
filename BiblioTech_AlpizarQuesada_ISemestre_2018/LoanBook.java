    private void btn_registerLoanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registerLoanActionPerformed
      archivoLoan = new File("Loan.dat");
   
        try {
            loanFile = new LoanFile(archivoLoan);
             String formato=calendar_starDate.getDateFormatString();
            Date fechainicial= calendar_starDate.getDate();
            SimpleDateFormat sdf= new SimpleDateFormat(formato);
             Loan loan=new Loan(tf_nameLoan.getText(), tf_idLoan.getText(), tf_codeloan.getText(),
             ""+ sdf.format(fechainicial));
       
        loanFile.addEndRecord(loan);
        lb_messageLoan.setVisible(true);
        lb_messageLoan.setText("El prestamo se origino correctamente");
        if(loanFile.quantityAvailable(tf_codeloan.getText())){
        }else{
            lb_messageLoan.setText("El libro no esta disponible");
        }
        tf_codeloan.setText("");
        tf_idLoan.setText("");
        tf_nameLoan.setText("");
        tf_nameLoan.setText("");

        } catch (FileNotFoundException ex) {
            Logger.getLogger(LoanBook.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(LoanBook.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btn_registerLoanActionPerformed

